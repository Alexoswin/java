public class Loops2 {
    
    public static void main (String[]args){
        System.out.println("--- while and do while loops in java ---");

                
        
        
        System.out.println("----- while looop -----");

        int i = 0;
        while(i <10){
            System.out.println(10*i);
            i++;
        }
        
        System.out.println("----- do while looop -----");

        i = 1;

        do{
            System.out.println(20*i);
            i ++;
        }
        while(i==0);
    }
}
